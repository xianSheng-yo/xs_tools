export const urlRegex =
  /^(?:(?:https?|ftp):)\/\/(?:\S+(?::\S*)?@)?(?:(?!(?:10|127)(?:\.\d{1,3}){3})(?!(?:169\.254|192\.168)(?:\.\d{1,3}){2})(?!172\.(?:1[6-9]|2\d|3[0-1])(?:\.\d{1,3}){2})(?:[1-9]\d?|1\d{2}|2[01]\d|22[0-3])(?:\.(?:1?\d{1,2}|2[0-4]\d|25[0-5])){2}(?:\.(?:[1-9]\d?|1\d{2}|2[0-4]\d|25[0-4]))|(?:localhost))(?::\d{2,5})?(?:[/?#]\S*)?$/;

export const isValidRegExp = (reg: RegExp, value: string) => reg.test(value);
